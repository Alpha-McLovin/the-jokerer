package com.example.thejokerer.navigation

/**
 * Enumeration class representing different types of navigation in the application.
 *
 * The `NavigationTypes` enum class defines possible types of navigation within the application,
 * such as bottom navigation and navigation rail. It's used to configure the layout and
 * appearance of the application based on the chosen navigation type and orientation of the screen.
 */
enum class NavigationTypes {
    BOTTOM_NAVIGATION, NAVIGATION_RAIL,
}
